    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
         int f=jTable1.rowAtPoint(evt.getPoint());
         int c=jTable1.columnAtPoint(evt.getPoint()); 
       //  System.out.println(""+modelo.getValueAt(f, 0)+"--"+modelo.getValueAt(f, 1));
         MCampeonato mCampeonatoSelec=listadoCampeonato.get(f);
         this.tfCodigo.setText(String.valueOf(mCampeonatoSelec.getCodigo()));
         
         this.tfNombre.setText(mCampeonatoSelec.getNombre());
         this.tfTemporada.setText(String.valueOf(mCampeonatoSelec.getTemporada()));
         this.dcFechaInicio.setDate(mCampeonatoSelec.getFechaInicio());
         this.dcFechaFinal.setDate(mCampeonatoSelec.getFechaFinal());
         this.cbTipo.setSelectedItem(modeloJtable.getValueAt(f, 5));
    }//GEN-LAST:event_jTable1MouseClicked

