    public final void actualizar(){
        String titulos[]={"Codigo","Nombre","Temp","FechaIni","FechaFin","Tipo"};
        modeloJtable=new DefaultTableModel(null, titulos);
        jTable1.setModel(modeloJtable);
        MCampeonato mCampeonato=new MCampeonato();
        
        listadoCampeonato=mCampeonato.obtenerCampeonatos();
        for (int i = 0; i < listadoCampeonato.size(); i++) {
            String registro[]={String.valueOf(listadoCampeonato.get(i).getCodigo()),listadoCampeonato.get(i).getNombre(),
                               String.valueOf(listadoCampeonato.get(i).getTemporada()),listadoCampeonato.get(i).getFechaInicio().toString(),
                                              listadoCampeonato.get(i).getFechaFinal().toString(),listadoCampeonato.get(i).getTipoCampeonato()};
            modeloJtable.addRow(registro);
        }
        limpiar();
    }
    public void limpiar(){
        this.tfCodigo.setText("");
        this.tfNombre.setText("");
        this.tfTemporada.setText("");
    }
    
    public final void cargarComboBoxTipo(){
        MTipo mTipo=new MTipo();
        ArrayList<MTipo> listaTipos=mTipo.obtenerTipos();
        for (int i = 0; i < listaTipos.size(); i++) {
            cbTipo.addItem(listaTipos.get(i));
        }
    }
    
    
